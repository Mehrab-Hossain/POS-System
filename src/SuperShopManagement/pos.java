    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String name = txtProductCode.getText();

        try {
             Class.forName("com.mysql.jdbc.Driver");
            String dBname = "hmehrab_supershop";
            String userName = "hmehrab_hmehrab";
            String password = "+#amishaon16+#";
            //con1 = (com.mysql.jdbc.Connection) (java.sql.Connection) java.sql.DriverManager.getConnection("jdbc:mysql://hmehrab.com:3306/"+dBname,userName,password);
            con1 = (Connection) java.sql.DriverManager.getConnection("jdbc:mysql://localhost/supershop","root","");

            insert = con1.prepareStatement("select * from product where barcode = ? ");
            insert.setString(1,name);

            rs = insert.executeQuery();

            while(rs.next()){
                int quantity;

                quantity = rs.getInt("quantity");

                int price  = Integer.parseInt(txtPrice.getText());
                int qtyUser  = Integer.parseInt(txtQuantity.getText());

                int tot = price * qtyUser;

                if(qtyUser> quantity){
                    JOptionPane.showMessageDialog(this, "Available Product is" + " = " + quantity);
                    JOptionPane.showMessageDialog(this, "Quantity is not Enough");
                    break;
                }

                else{
                    model = (DefaultTableModel)jTable1.getModel();

                    model.addRow(new Object[]{
                        txtProductCode.getText(),
                        txtProductName.getText(),
                        txtPrice.getText(),
                        txtQuantity.getText(),
                        tot,
                    });
                    int sum=0;

                    for(int i=0;i<jTable1.getRowCount();i++){
                        sum = sum + Integer.parseInt(jTable1.getValueAt(i, 4).toString());
                    }
                    txtSubTotal.setText(Integer.toString(sum));

                    txtProductCode.setText("");
                    txtProductName.setText("");
                    txtPrice.setText("");
                    txtQuantity.setText("");
                }

            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

