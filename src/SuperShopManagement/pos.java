    private void sales(){
        
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        
        String subtot = txtSubTotal.getText();
        String pay = txtPay.getText();
        String cashier = txtloged.getText();
        String balance = txtBalance.getText();
        int lastInsertId=0;
        try {
                 Class.forName("com.mysql.jdbc.Driver");
                con1 = (Connection) java.sql.DriverManager.getConnection("jdbc:mysql://localhost/supershop","root","");
                String query = "insert into sales(date,cashier,subtotal,pay,balance) values(?,?,?,?,?)";
                insert = con1.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            
                insert.setString(1,date);
                insert.setString(2,cashier);
                insert.setString(3,subtot);
                insert.setString(4,pay);
                insert.setString(5,balance);
                insert.executeUpdate();
                // table_update_category();
                //JOptionPane.showMessageDialog(null, "Sales Added");
                
                ResultSet generateKeyResult = insert.getGeneratedKeys();//last inserted id
                
                if(generateKeyResult.next()){
                    lastInsertId = generateKeyResult.getInt(1);
                }
                JOptionPane.showMessageDialog(this, lastInsertId);
                
                int rows = jTable1.getRowCount();
                String query2 = "insert into sales_product(sales_id,product_id,sale_price,quantity,total) values(?,?,?,?,?)";
                insert = con1.prepareStatement(query2);
                String product_id="";
                String price="";
                String quantity="";
                int total=0;
                for(int i=0;i<rows;i++){
                    product_id = (String)jTable1.getValueAt(i, 0);
                    price = (String)jTable1.getValueAt(i, 2);
                    quantity = (String)jTable1.getValueAt(i, 3);
                    total = (int)jTable1.getValueAt(i, 4);
                    
                    insert.setInt(1, lastInsertId);
                    insert.setString(2, product_id);
                    insert.setString(3, price);
                    insert.setString(4, quantity);
                    insert.setInt(5, total);
                    insert.executeUpdate();
                    
                    
                }
                insert.addBatch();
                JOptionPane.showMessageDialog(this,"Record added to Sales_Product Table");
                
                String query3 = "update product set quantity = quantity-? where barcode=?";
                insert = con1.prepareStatement(query3);
                
                for(int i=0;i<rows;i++){
                    product_id = (String)jTable1.getValueAt(i, 0);
                    quantity = (String)jTable1.getValueAt(i, 3);
                
                    
                    insert.setString(1, quantity);
                    insert.setString(2, product_id);
                    insert.executeUpdate();
                    
                    
                }
                
                

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }
    
